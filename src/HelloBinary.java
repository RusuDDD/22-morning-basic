
public class HelloBinary {
    public static void main(String[] args) {
        int hex = 0xABC;
        int hex1 = 0x100;
        System.out.println(hex);
        System.out.println(hex1);
        int binary = 0B10_01_10_01;
        System.out.println(binary);
        int octal = 0757;
        System.out.println(octal);
    }
}
